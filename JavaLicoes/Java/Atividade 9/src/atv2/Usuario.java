package atv2;

public class Usuario {
	public String tema;
	public boolean notif;
	public int vol;
	
	public String isTema() {
		return tema;
	}
	public void setTema(String tema) {
		this.tema = tema;
	}
	public boolean isNotif() {
		return notif;
	}
	public void setNotif(boolean notif) {
		this.notif = notif;
	}
	public int getVol() {
		return vol;
	}
	public void setVol(int vol) {
		this.vol = vol;
	}
	
	
}
